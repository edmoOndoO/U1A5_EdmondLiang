    private void btncalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalculateActionPerformed
        // TODO add your handling code here:
        //In case of error try sends error message instead of breaking the program
        try {
            //Getting money amount from the user
            double amountMoney = Double.parseDouble(txtfieldEnterCurrency.getText());
            txtfieldAmount.setText(Double.toString(amountMoney));

            //Temporary variable for checking if the user input is valid.
            boolean valid = false;
            //turns the double to string for check.
            String stringAmount = Double.toString(amountMoney);

            //Using the index of the . and the length of the amount, the amount of decimals can be found.
            int integerPlaces = stringAmount.indexOf(".");
            
            //To get the decimal places, you do (length of string) - (index of string) - 1
            
            int decimalPlaces =  stringAmount.length() -  integerPlaces -1;

            //Checker if amount of decimals are less than 2. If true then the currency amount is valid.
            if (decimalPlaces <= 2) {
                valid = true;
            }
            
            //If the user input is valid do, turn to coins
            if (valid) {
                double moneyAmount = amountMoney;
                //Default values in case they are not used, the coin value will be 0
                int penny = 0;
                int nickel = 0;
                int dime = 0;
                int quarter = 0;
                int loonie = 0;
                int toonie = 0;

                //Get amount toonies used by dividing, then getting the left over amount for next calculation, then resetting the glitchy format of the amount.
                toonie = (int) (moneyAmount/2.00);
                moneyAmount = moneyAmount - 2.00 * toonie;
                moneyAmount = Math.round(moneyAmount * 100.0) / 100.0;
                
                
                //Get amount loonie used by dividing, then getting the left over amount for next calculation, then resetting the format of the amount.
                loonie = (int) (moneyAmount/1);
                moneyAmount = moneyAmount - 1.00 * loonie;
                moneyAmount = Math.round(moneyAmount * 100.0) / 100.0;

                //Get amount quarter used by dividing, then getting the left over amount for next calculation, then resetting the format of the amount.
                quarter = (int) (moneyAmount/0.25);
                moneyAmount = moneyAmount - 0.25 * quarter;
                moneyAmount = Math.round(moneyAmount * 100.0) / 100.0;

                //Get amount dime used by dividing, then getting the left over amount for next calculation, then resetting the format of the amount.
                dime = (int) (moneyAmount/0.10);
                moneyAmount = moneyAmount - 0.10 * dime;
                moneyAmount = Math.round(moneyAmount * 100.0) / 100.0;

                //Get amount nickel used by dividing, then getting the left over amount for next calculation, then resetting the format of the amount.
                nickel = (int) (moneyAmount/0.05);
                moneyAmount = moneyAmount - 0.05 * nickel;
                moneyAmount = Math.round(moneyAmount * 100.0) / 100.0;

                //Get amount penny used by dividing, then getting the left over amount for next calculation, then resetting the format of the amount.
                penny = (int) (moneyAmount/0.01);

                //Have to cast all variable to string because set text looks for string and variables are integers
                txtfieldPennies.setText(Integer.toString(penny));
                txtfieldQuarters.setText(Integer.toString(quarter));
                txtfieldNickels.setText(Integer.toString(nickel));
                txtfieldLoonies.setText(Integer.toString(loonie));
                txtfieldDimes.setText(Integer.toString(dime));
                txtfieldToonies.setText(Integer.toString(toonie));
                //Clears the enter currency text field for easier user access
                txtfieldEnterCurrency.setText("         ");
                
                //Display results
                resultAppear();
            }
            else {
                txtfieldAmount.setText("Format Invalid"); //If format isnt correct displays Format Invalid.
            }
        }
        catch (Exception e) {
            txtfieldAmount.setText("Amount Invalid"); //If format isnt correct displays Format Invalid.
        }
    }//GEN-LAST:event_btncalculateActionPerformed

